'use strict';

angular.module('stepApp')
    .factory('HrEmpTransferApplInfo', function ($resource, DateUtils) {
        return $resource('api/hrEmpTransferApplInfos/:id', {}, {
            'query': { method: 'GET', isArray: true},
            'get': {
                method: 'GET',
                transformResponse: function (data) {
                    data = angular.fromJson(data);
                    data.officeOrderDate = DateUtils.convertLocaleDateFromServer(data.officeOrderDate);
                    data.createDate = DateUtils.convertLocaleDateFromServer(data.createDate);
                    data.updateDate = DateUtils.convertLocaleDateFromServer(data.updateDate);
                    return data;
                }
            },
            'update': {
                method: 'PUT',
                transformRequest: function (data) {
                    data.officeOrderDate = DateUtils.convertLocaleDateToServer(data.officeOrderDate);
                    data.createDate = DateUtils.convertLocaleDateToServer(data.createDate);
                    data.updateDate = DateUtils.convertLocaleDateToServer(data.updateDate);
                    return angular.toJson(data);
                }
            },
            'save': {
                method: 'POST',
                transformRequest: function (data) {
                    data.officeOrderDate = DateUtils.convertLocaleDateToServer(data.officeOrderDate);
                    data.createDate = DateUtils.convertLocaleDateToServer(data.createDate);
                    data.updateDate = DateUtils.convertLocaleDateToServer(data.updateDate);
                    return angular.toJson(data);
                }
            }
        });
    }).factory('HrEmpTransferApplInfoApprover', function ($resource) {
        return $resource('api/hrEmpTransferApplInfosApprover/:id', {},
            {
                'update': { method: 'POST'},
                'query': { method: 'GET', isArray: true}
            });
    }).factory('HrEmpTransferApplInfoApproverLog', function ($resource) {
        return $resource('api/hrEmpTransferApplInfosApprover/log/:entityId', {},
            {
                'update': { method: 'POST'},
                'query': { method: 'GET', isArray: true}
            });
    });
